//Create a function that takes an integer as an argument and returns
//"Even" for even numbers or "Odd" for odd numbers.

function even_or_odd(number) {
//conditional deciding whether number is "Even" or "Odd"
    if(number % 2===0){
        console.log("Even")
    }else{
        console.log("Odd")
    }

}
  

even_or_odd(6)//"Even"

